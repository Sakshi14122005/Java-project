package printusingvariable;

public class TemperatureSensorReadings {
    public static void main(String[] args) {
        // Declare and assign temperature readings
        byte hotTemp = 45;
        byte coldTemp = -20;

        // Print the temperature readings
        System.out.println("Hot Region Temperature: " + hotTemp);
        System.out.println("Cold Region Temperature: " + coldTemp);
    }
}
