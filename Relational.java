package abc;

public class Relational {
	
		public static void main(String[] args)
		{
			
			System.out.println(20<21);
			System.out.println(20>21);
			System.out.println(20>=21);
			System.out.println(20<=21);
			System.out.println(20==21);
			System.out.println(20!=21);
			
		}

	}

