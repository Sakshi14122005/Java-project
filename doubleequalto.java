package codechef;
import java.util.*;

//Main class
public class doubleequalto {

 public static void main(String[] args) {
     // Declaring an integer variable
     int number = 10;

     // Checking if the number is equal to 10
     if (number == 10) {
         System.out.println("The number is exactly 10."); // Executes if condition is true
     } else {
         System.out.println("The number is not 10."); // Executes if condition is false
     }

     // This statement executes regardless of the condition
     System.out.println("Comparison check completed.");
 }
}
